def mergeSort(sequence):
    if len(sequence) < 2:
        return sequence
    m = len(sequence) // 2
    return merge(mergeSort(sequence[:m]), mergeSort(sequence[m:]))


def merge(left, right):
    result = []
    i = j = 0
    while i < len(left) and j < len(right):
        if left[i] < right[j]:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1
    result += left[i:]
    result += right[j:]

    return result




